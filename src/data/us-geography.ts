// Generate comprehensive area served array for all US states and major cities
export const generateAreaServed = (includeAllStates: boolean = true, includeMajorCities: boolean = true) => {
  const areaServed = ['United States'];
  
  if (includeAllStates) {
    // Add all 50 states + DC
    areaServed.push(...US_STATES.map(state => state.name));
  }
  
  if (includeMajorCities) {
    // Add major cities from all states
    Object.values(MAJOR_CITIES_BY_STATE).forEach(cities => {
      areaServed.push(...cities);
    });
  }
  
  return areaServed;
};

// Generate state-specific keywords for SEO
export const generateStateKeywords = (baseKeywords: string[], targetStates?: string[]) => {
  const states = targetStates || US_STATES.map(state => state.name);
  const stateKeywords: string[] = [];
  
  baseKeywords.forEach(keyword => {
    states.forEach(state => {
      stateKeywords.push(`${keyword} ${state}`);
      // Also add abbreviation versions for some states
      const stateObj = US_STATES.find(s => s.name === state);
      if (stateObj && stateObj.name !== stateObj.abbreviation) {
        stateKeywords.push(`${keyword} ${stateObj.abbreviation}`);
      }
    });
  });
  
  return stateKeywords;
};

